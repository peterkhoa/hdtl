using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.UI.WebControls;

namespace hoachdinhtuonglai.Data.BL
{
    public static class Utility
    {
        public static void CreateAlert(Label label, string message)
        {
            label.Text = "<span class='ui-icon ui-icon-alert'></span>" + message;
            label.CssClass = "message ui-state-error ui-corner-all";
        }

        public static void CreateInfo(Label label, string message)
        {
            label.Text = "<span class='ui-icon ui-icon-info'></span>" + message;
            label.CssClass = "message ui-state-highlight ui-corner-all";
        }
    }
}